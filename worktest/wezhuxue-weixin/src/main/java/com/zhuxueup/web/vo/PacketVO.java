package com.zhuxueup.web.vo;

import java.io.Serializable;

public class PacketVO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8035772614649467314L;
	
	private String id;
	private String packetMoney;
	private String createTime;
	private String leftMoney;
	private String snagMoney;
	private String status;
	public String getId() {
		return id;
	}
	public String getPacketMoney() {
		return packetMoney;
	}
	public String getCreateTime() {
		return createTime;
	}
	public String getLeftMoney() {
		return leftMoney;
	}
	public String getSnagMoney() {
		return snagMoney;
	}
	public String getStatus() {
		return status;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setPacketMoney(String packetMoney) {
		this.packetMoney = packetMoney;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public void setLeftMoney(String leftMoney) {
		this.leftMoney = leftMoney;
	}
	public void setSnagMoney(String snagMoney) {
		this.snagMoney = snagMoney;
	}
	public void setStatus(String status) {
		this.status = status;
	}

}
